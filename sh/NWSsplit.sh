#!/bin/sh
###############################################################

###############################################################
# generation of the simulation
if [ -z "$GEN" ]; then
    GEN=1
fi
###############################################################
# name of the series
if [ -z "$SERIES" ]; then
    SERIES=cusp
fi
###############################################################
# number of MPI processes
if [ -z "$PROCS" ]; then
    PROCS=64
fi
###############################################################

###############################################################
FULL=${SERIES}-gen${GEN}.lst
LIST=${SERIES}-gen${GEN}-split.lst
###############################################################
split -d -n l/$PROCS $FULL $LIST
###############################################################

###############################################################
exit 0
###############################################################
